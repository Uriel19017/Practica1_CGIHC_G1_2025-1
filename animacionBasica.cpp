// Moves/alters the camera positions based on user input
void DoMovement()
{

	// Camera controls
	if (keys[GLFW_KEY_W] || keys[GLFW_KEY_UP])
	{
		camera.ProcessKeyboard(FORWARD, deltaTime);

	}

	if (keys[GLFW_KEY_S] || keys[GLFW_KEY_DOWN])
	{
		camera.ProcessKeyboard(BACKWARD, deltaTime);


	}

	if (keys[GLFW_KEY_A] || keys[GLFW_KEY_LEFT])
	{
		camera.ProcessKeyboard(LEFT, deltaTime);


	}

	if (keys[GLFW_KEY_D] || keys[GLFW_KEY_RIGHT])
	{
		camera.ProcessKeyboard(RIGHT, deltaTime);


	}

	if (keys[GLFW_KEY_T])
	{
		pointLightPositions[0].x += 0.01f;
	}
	if (keys[GLFW_KEY_G])
	{
		pointLightPositions[0].x -= 0.01f;
	}

	if (keys[GLFW_KEY_Y])
	{
		pointLightPositions[0].y += 0.01f;
	}

	if (keys[GLFW_KEY_H])
	{
		pointLightPositions[0].y -= 0.01f;
	}
	if (keys[GLFW_KEY_U])
	{
		pointLightPositions[0].z -= 0.1f;
	}
	if (keys[GLFW_KEY_J])
	{
		pointLightPositions[0].z += 0.01f;
	}

	if (keys[GLFW_KEY_B] && !isJumping)  // Start jump with space key
	{
		isJumping = true;
		jumpTime = 0.0f;
	}

	if (keys[GLFW_KEY_B] && !isBallJumping)  // Start jump for ball with space key
	{
		isBallJumping = true;
		jumpTime = 0.0f;
	}
	
}

// Is called whenever a key is pressed/released via GLFW
void KeyCallback(GLFWwindow *window, int key, int scancode, int action, int mode)
{
	if (GLFW_KEY_ESCAPE == key && GLFW_PRESS == action)
	{
		glfwSetWindowShouldClose(window, GL_TRUE);
	}

	if (key >= 0 && key < 1024)
	{
		if (action == GLFW_PRESS)
		{
			keys[key] = true;
		}
		else if (action == GLFW_RELEASE)
		{
			keys[key] = false;
		}
	}

	if (keys[GLFW_KEY_SPACE])
	{
		active = !active;
		if (active)
		{
			Light1 = glm::vec3(1.0f, 1.0f, 0.0f);
			
		}
		else
		{
			Light1 = glm::vec3(0);//Cuado es solo un valor en los 3 vectores pueden dejar solo una componente
		}
	}
	if (keys[GLFW_KEY_N])
	{
		AnimBall = !AnimBall;
		
	}

	if (keys[GLFW_KEY_M])
	{
		AnimBallX = !AnimBallX;
	}
}
void Animation()
{
	// Animación en el eje Y
	if (AnimBall)
	{
		rotBall += 0.9f;
		if (rotBall >= 360.0f)
			rotBall = 0.0f; // Resetear después de un giro completo
	}

	// Animación en el eje X
	if (AnimBallX)
	{
		rotBallX += 0.9f;
		if (rotBallX >= 360.0f)
			rotBallX = 0.0f; // Resetear después de un giro completo
	}

	// Rotation around the Y axis (circular movement)
	rotAngle += deltaTime * 1.0f; // Adjust speed of rotation if needed
	if (rotAngle > 2 * M_PI)
		rotAngle -= 2 * M_PI;

	// Jump animation (sinusoidal movement)
	if (isJumping)
	{
		jumpTime += deltaTime;
		height = sin(jumpTime * 5.0f);  // Adjust jump speed and height
		if (jumpTime >= M_PI / 5.0f)    // End jump after one sine wave
		{
			isJumping = false;
			height = 0.0f;
		}
	}

	// Rotation for the ball (circular movement)
	ballRotAngle += deltaTime * 1.0f; // Adjust speed of rotation if needed
	if (ballRotAngle > 2 * M_PI)
		ballRotAngle -= 2 * M_PI;

	// Ball jump animation (sinusoidal movement)
	if (isBallJumping)
	{
		jumpTime += deltaTime;
		ballHeight = -sin(jumpTime * 1.0f); // Adjust jump speed and height
		if (jumpTime >= M_PI / 5.0f) // End jump after one sine wave
		{
			isBallJumping = false;
			ballHeight = 0.0f;
		}
	}
}



void MouseCallback(GLFWwindow *window, double xPos, double yPos)
{
	if (firstMouse)
	{
		lastX = xPos;
		lastY = yPos;
		firstMouse = false;
	}

	GLfloat xOffset = xPos - lastX;
	GLfloat yOffset = lastY - yPos;  // Reversed since y-coordinates go from bottom to left

	lastX = xPos;
	lastY = yPos;

	camera.ProcessMouseMovement(xOffset, yOffset);
}